// Get all countries as array for dropdown
function getAllCountries() {
    return Object.keys(countryPhoneData).map(code => ({
        code: code,
        name: countryPhoneData[code].name,
        flag: countryPhoneData[code].flag,
        display: `${countryPhoneData[code].flag} ${code} (${countryPhoneData[code].name})`
    })).sort((a, b) => a.name.localeCompare(b.name));
}

// Validate phone number based on country code
function validatePhoneNumber(countryCode, phoneNumber) {
    // Remove all non-digit characters for validation
    const digits = phoneNumber.replace(/\D/g, '');
    
    if (!countryCode || !countryPhoneData[countryCode]) {
        return { valid: false, message: 'Please select a valid country' };
    }
    
    const country = countryPhoneData[countryCode];
    
    if (digits.length < country.min || digits.length > country.max) {
        return {
            valid: false,
            message: `Phone number must be ${country.min}-${country.max} digits for ${country.name}`
        };
    }
    
    if (country.pattern && !country.pattern.test(digits)) {
        return {
            valid: false,
            message: `Invalid phone number format for ${country.name}`
        };
    }
    
    return { valid: true, message: '' };
}

// Get placeholder based on country
function getPhonePlaceholder(countryCode) {
    if (!countryCode || !countryPhoneData[countryCode]) {
        return 'Phone number';
    }
    
    const country = countryPhoneData[countryCode];
    const placeholder = '0'.repeat(country.min);
    return `e.g., ${placeholder}`;
}
